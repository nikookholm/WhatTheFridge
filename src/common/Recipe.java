package common;

public class Recipe {

	int ID;
	String name;
	int[] ingredients;
	String description;
	
	public int getID(){
		return ID;
	}
	
	public String getName(){
		return name;
	}
	
	public int[] ingredients(){
		return ingredients;
	}
	
	public String getDescription(){
		return description;
	}
	
}
